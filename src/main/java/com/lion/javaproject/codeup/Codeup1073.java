package com.lion.javaproject.codeup;

import java.util.Scanner;

public class Codeup1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(true) {
            int val = sc.nextInt();
          if (val == 0)
            break;
         else
            System.out.println(val);
        }
    }
}