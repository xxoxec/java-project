package com.lion.javaproject.week2.day4;

public class IncreaseOperator2 {
    public static void main(String[] args) {
        int i = 10;
        // ++가 앞에 오는 경우 일단 먼저 올림,내림
        System.out.println(++i);
        System.out.println(i);

        System.out.println(--i);
        System.out.println(i);
    }
}
