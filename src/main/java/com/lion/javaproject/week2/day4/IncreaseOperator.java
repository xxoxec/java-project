package com.lion.javaproject.week2.day4;

public class IncreaseOperator {
    public static void main(String[] args) {
        int i = 10;
        // ++가 뒤에 오는 경우 나중에 올림,내림
        System.out.println(i++);
        System.out.println(i);

        System.out.println(i--);
        System.out.println(i);
    }
}
